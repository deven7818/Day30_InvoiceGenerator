package com.invoicegenerator;


public class Rides {
	public double distance;
	public double time;

	/**
	 * Constructor
	 * 
	 * @param distance - Distance traveled by cab
	 * @param time     - time taken to travel
	 */
	public Rides(double distance, double time) {
		super();
		this.distance = distance;
		this.time = time;
	}

}